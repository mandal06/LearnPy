print("---types of int---")

a = int(1)
b = int(5.22)
c = int("10")

print(a)  # Output: 1
print(b)  # Output: 5
print(c)  # Output: 10

print("\n")
print("---types of float---")

x = float(1)
y = float(5.22)
z = float("10")

print(x)  # Output: 1
print(y)  # Output: 5
print(z)  # Output: 10

print("\n")
print("---types of String---")
